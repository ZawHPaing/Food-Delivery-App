// Helper function to get restaurant with full menu
export function getRestaurantWithMenu(restaurantId: number): RestaurantWithMenu | null {
  const restaurant = restaurants.find(r => r.id === restaurantId);
  if (!restaurant) return null;

  const items = menuItemsByRestaurant[restaurantId] || [];
  
  // Get unique categories from menu items
  const categoryMap = new Map<number, Category>();
  items.forEach(item => {
    item.categories.forEach(cat => {
      categoryMap.set(cat.id, cat);
    });
  });

  const menuCategories = Array.from(categoryMap.values());

  const menu: MenuWithItems = {
    id: restaurantId,
    restaurant_id: restaurantId,
    name: "Main Menu",
    is_active: true,
    items,
  };

  return {
    ...restaurant,
    menus: [menu],
    categories: menuCategories,
  };
}

// Helper function to get items by category
export function getItemsByCategory(restaurantId: number, categoryId: number): MenuItemWithCategory[] {
  const items = menuItemsByRestaurant[restaurantId] || [];
  return items.filter(item => item.categories.some(cat => cat.id === categoryId));
}

// Helper function to get popular items
export function getPopularItems(restaurantId: number): MenuItemWithCategory[] {
  const items = menuItemsByRestaurant[restaurantId] || [];
  return items.filter(item => item.isPopular);
}

// Helper function to get restaurant reviews
export function getRestaurantReviews(restaurantId: number): Review[] {
  return reviewsByRestaurant[restaurantId] || [];
}

